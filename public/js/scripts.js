async function addpts(){
	const w = {
		username: user.username,
	};
	const win = {
		method: 'POST',
		body: JSON.stringify(w),
		headers: {
        "Content-type": "application/json; charset=UTF-8"
		},
	};
	fetch('/addpts', win ).then();
};

